package com.readJson;

public class StepDetail {
    private String event;
    private String value;
    private String variable;
    private String locator;
    private String startTCIDText;
    private String startTCIDValue;

    // Getters and Setters
    public String getEvent() { return event; }
    public void setEvent(String event) { this.event = event; }

    public String getValue() { return value; }
    public void setValue(String value) { this.value = value; }

    public String getVariable() { return variable; }
    public void setVariable(String variable) { this.variable = variable; }

    public String getLocator() { return locator; }
    public void setLocator(String locator) { this.locator = locator; }

    public String getStartTCIDText() { return startTCIDText; }
    public void setStartTCIDText(String startTCIDText) { this.startTCIDText = startTCIDText; }

    public String getStartTCIDValue() { return startTCIDValue; }
    public void setStartTCIDValue(String startTCIDValue) { this.startTCIDValue = startTCIDValue; }
}
